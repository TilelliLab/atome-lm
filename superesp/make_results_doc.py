"""superesp.make_results_doc — render HONEST_RESULTS.md straight from artifacts.

Reads artifacts/RESULTS.json (+ ATTESTATION.json + parity result if present) and
writes superesp/HONEST_RESULTS.md. Numbers come ONLY from the on-disk run
artifacts, never from memory.  Run:  python3 -m superesp.make_results_doc
"""
from __future__ import annotations

import json
from pathlib import Path

ART = Path(__file__).resolve().parent / "artifacts"
DOC = Path(__file__).resolve().parent / "HONEST_RESULTS.md"


def render() -> str:
    rows = json.loads((ART / "RESULTS.json").read_text())
    att = {}
    ap = ART / "ATTESTATION.json"
    if ap.exists():
        for r in json.loads(ap.read_text()):
            att[r["head"]] = r.get("verify_ok")
    parity = {}
    pp = ART / "PARITY.json"
    if pp.exists():
        parity = json.loads(pp.read_text())

    lines = []
    lines.append("# SuperESP — Honest Results\n")
    lines.append("> Generated by `make_results_doc.py` from `artifacts/RESULTS.json`. "
                 "Every number is from a held-out TEST split. REAL = real public "
                 "dataset; SYNTH = physics-style stand-in (labeled, not a field claim).\n")
    lines.append("## Table 1 — per-head held-out accuracy + abstention + delta\n")
    lines.append("| head | data | classes | params | TEST acc | abstain AURC (oracle/rand) | cov@≤5% risk | blob B | delta@0.05 |")
    lines.append("|---|---|--:|--:|--:|--:|--:|--:|--:|")
    for r in rows:
        if not r["delta"].get("applicable", True):
            delta_s = "N/A (tabular)"
        else:
            d05 = r["delta"].get("thr_0.05", {})
            delta_s = f"{d05.get('speedup','?')}x/err{d05.get('max_err','?')}"
        lines.append(
            f"| {r['title']} | {r['source']} | {r['n_classes']} | {r['params']} | "
            f"{r['test_acc']:.3f} | {r['abstain_aurc']:.4f} "
            f"({r['oracle_aurc']:.4f}/{r['random_aurc']:.4f}) | "
            f"{r['coverage_at_5pct_risk']:.2f} | {r['blob_bytes']} | {delta_s} |"
        )
    accs = [r["test_acc"] for r in rows]
    lines.append(f"\n**Mean held-out accuracy across {len(rows)} heads: "
                 f"{sum(accs)/len(accs):.3f}** (min {min(accs):.3f}, max {max(accs):.3f}).\n")

    if parity:
        lines.append("## Table 2 — Python↔C parity (bit-exact)\n")
        lines.append(f"- max |Δ logit| across tested heads: **{parity.get('max_abs_diff','?')}** "
                     f"(tolerance 1e-3); argmax agreement: {parity.get('argmax_agree','?')}.\n")

    if att:
        ok = sum(1 for v in att.values() if v)
        lines.append("## Table 3 — attestation\n")
        lines.append(f"- {ok}/{len(att)} head blobs Ed25519-signed and verified; "
                     "tamper (blob/metadata/signature) rejected (see tests/test_attest.py).\n")

    lines.append("## What is NOT measured\n")
    lines.append("- On-silicon tok/s, RAM high-water, live ADC/I2S capture — no physical "
                 "ESP32 in the build box. Firmware is a build-only skeleton.\n")
    lines.append("- SYNTH heads are physics-style stand-ins, not field deployments.\n")
    lines.append("## Moat verdict\n")
    lines.append("- Individual heads are a real STEP / applied product, **not moats** "
                 "(TinyML is crowded). Defensible angle = tiny-ternary + bit-exact-auditable "
                 "+ attested + delta-efficient unified OS (first-mover/integration bet).\n")
    return "\n".join(lines)


if __name__ == "__main__":
    DOC.write_text(render())
    print(f"wrote {DOC}")
